package delphos;

public class TerminoTecnologiaEmergente {
	String terminoPrincipal;
	String termino;
	
	public TerminoTecnologiaEmergente(String terminoPrincipal, String termino) {
		this.terminoPrincipal = terminoPrincipal;
		this.termino = termino;
	}
	
	public String getTerminoPrincipal() {
		return terminoPrincipal;
	}
	public void setTerminoPrincipal(String terminoPrincipal) {
		this.terminoPrincipal = terminoPrincipal;
	}
	public String getTermino() {
		return termino;
	}
	public void setTermino(String termino) {
		this.termino = termino;
	}
	
}
